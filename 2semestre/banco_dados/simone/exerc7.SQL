SELECT T1.O_TOTALPRICE
FROM ORDERS AS T1
LEFT JOIN CUSTOMER AS T2
ON T1.O_CUSTKEY = T2.C_CUSTKEY
LEFT JOIN NATION AS T3
ON T2.C_NATIONKEY = T3.N_NATIONKEY
WHERE T3.N_NAME = 'BRAZIL'

UNION SELECT P_RETAILPRICE FROM PART;



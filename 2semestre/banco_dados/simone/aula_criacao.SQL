CREATE TABLE MEDICOS_TBC ( 
    CRM INT PRIMARY KEY,
    SALARIO NUMERIC(15,2),
    NOME_MEDICO VARCHAR(50),
    ESPECIALIDADE VARCHAR(50) );
    
CREATE TABLE PACIENTES_TBC (
    CPF BIGINT PRIMARY KEY,
    NOME_PACIENTE VARCHAR(50),
    TELEFONE BIGINT,
    ENDERECO VARCHAR(100),
    RG BIGINT,
    HORA_VISITA VARCHAR(6),
    NUMERO_QUARTO INT,
    ANDAR INT,
    CRM INT REFERENCES MEDICOS_TBC ON UPDATE CASCADE ON DELETE CASCADE);

CREATE TABLE TRATAMENTO_TBC (
    DESCRICAO VARCHAR(250),
    CRM INT REFERENCES MEDICOS_TBC,
    CPF BIGINT REFERENCES PACIENTES_TBC ON UPDATE CASCADE ON DELETE CASCADE);




/* EXERCICIO 1 */

    SELECT T1.P_NAME
    FROM PART AS T1
    WHERE P_RETAILPRICE < (
            SELECT AVG( C_ACCTBAL ) FROM CUSTOMER );

/* EXERCICIO 2 */

    SELECT T1.P_NAME
    FROM PART AS T1
    GROUP BY T1.P_NAME
    HAVING AVG( T1.P_RETAILPRICE) < (
        SELECT AVG( C_ACCTBAL ) FROM CUSTOMER);

/* EXERCICIO 3 */

    SELECT T1.O_ORDERSTATUS
    FROM ORDERS AS T1
    LEFT JOIN CUSTOMER AS T2
    ON T1.O_CUSTKEY = T2.C_CUSTKEY
    WHERE T2.C_NATIONKEY = ( SELECT N_NATIONKEY FROM NATION WHERE N_NAME = 'BRAZIL');

/* EXERCICIO 4 */
    SELECT T1.O_COMMENT
    FROM ORDERS AS T1
    
    LEFT JOIN LINEITEM AS T2
    ON T1.O_ORDERKEY = T2.L_ORDERKEY
    
    WHERE T2.L_QUANTITY = ANY(SELECT P_SIZE FROM PART WHERE P_TYPE = 'LARGE POLISHED COPPER')
    AND T2.L_SHIPDATE BETWEEN '1998-07-27' AND '1998-12-31'


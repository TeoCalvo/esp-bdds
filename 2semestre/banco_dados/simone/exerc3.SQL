    WHERE C_NATIONKEY = 24 AND
    C_ACCTBAL > 9990;

/* EXERCICIO 4 */

    SELECT C_NAME,
	   TO_CHAR( SQRT(C_ACCTBAL), '$ 999.99'),
           LENGTH( C_COMMENT )
    FROM CUSTOMER
    WHERE C_NATIONKEY = 24 AND
    C_ACCTBAL > 9990;

